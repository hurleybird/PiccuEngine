const char* bloomMergeFragmentSrc =
"#version 330 core\n"
"\n"
"in vec2 outuv;\n"
"out vec4 color;\n"
"\n"
"uniform sampler2D MergeCur;\n"
"uniform sampler2D MergePrev;\n"
"uniform float bloomSpread;\n"
"\n"
"vec3 Blur3x3(sampler2D source, vec2 uv, vec2 scale)\n"
"{\n"
"	const float weights[9] = float[9](0.0625, 0.125, 0.0625, 0.125, 0.25, 0.125, 0.0625, 0.125, 0.0625);\n"
"	vec3 sum = vec3(0.0);\n"
"	int i = 0;\n"
"	for (int y = -1; y <= 1; y++)\n"
"	{\n"
"		for (int x = -1; x <= 1; x++, i++)\n"
"			sum += texture(source, uv + vec2(x, y) * scale).rgb * weights[i];\n"
"	}\n"
"	return sum;\n"
"}\n"
"\n"
"void main()\n"
"{\n"
"	float blurScale = 1.5;\n"
"	vec3 curSample = texture(MergeCur, outuv).rgb;\n"
"	vec2 scaleUpsampled = vec2(blurScale) / vec2(textureSize(MergePrev, 0));\n"
"	vec3 upsampled = Blur3x3(MergePrev, outuv, scaleUpsampled);\n"
"	color = vec4(mix(curSample, upsampled, bloomSpread), 1.0);\n"
"}\n"
"";

const char* bloomCompositeFragmentSrc =
"#version 330 core\n"
"\n"
"in vec2 outuv;\n"
"out vec4 color;\n"
"\n"
"uniform sampler2D heh;\n"
"uniform sampler2D bloom;\n"
"uniform sampler2D scene_source;\n"
"uniform float gamma;\n"
"uniform float bloom_intensity;\n"
"\n"
"void main()\n"
"{\n"
"	vec4 sourcecolor = texture(heh, outuv);\n"
"	vec3 scene_color = texture(scene_source, outuv).rgb;\n"
"	vec3 bloomcolor = texture(bloom, outuv).rgb * bloom_intensity;\n"
"	vec3 changed = abs(sourcecolor.rgb - scene_color);\n"
"	float changed_amount = max(max(changed.r, changed.g), changed.b);\n"
"	float scene_mask = 1.0 - smoothstep(0.002, 0.03, changed_amount);\n"
"	color = vec4(pow(max(sourcecolor.rgb + bloomcolor * scene_mask, vec3(0.0)), vec3(gamma)), 1.0);\n"
"}\n"
"";

