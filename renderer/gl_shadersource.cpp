const char* downsampleFragmentSrc =
"#version 330 core\n"
"\n"
"out vec4 color;\n"
"\n"
"uniform sampler2D heh;\n"
"uniform float gamma;\n"
"uniform ivec2 dest_origin;\n"
"\n"
"vec3 ToDisplay(vec3 sourcecolor)\n"
"{\n"
"	return pow(max(sourcecolor, vec3(0.0)), vec3(gamma));\n"
"}\n"
"\n"
"vec3 FromDisplay(vec3 sourcecolor)\n"
"{\n"
"	return pow(max(sourcecolor, vec3(0.0)), vec3(1.0 / max(gamma, 0.0001)));\n"
"}\n"
"\n"
"vec4 WronskiFilter(ivec2 dst)\n"
"{\n"
"	const float d0 = 0.75777;\n"
"	const float d1 = 2.907;\n"
"	const float w0 = 0.37487566;\n"
"	const float w1 = -0.12487566;\n"
"	vec2 inv_source_size = 1.0 / vec2(textureSize(heh, 0));\n"
"	vec2 uv = (vec2(dst) * 2.0 + vec2(1.0)) * inv_source_size;\n"
"	vec4 c0 = texture(heh, uv + vec2( d0,  d0) * inv_source_size);\n"
"	vec4 c1 = texture(heh, uv + vec2(-d0,  d0) * inv_source_size);\n"
"	vec4 c2 = texture(heh, uv + vec2( d0, -d0) * inv_source_size);\n"
"	vec4 c3 = texture(heh, uv + vec2(-d0, -d0) * inv_source_size);\n"
"	vec4 c4 = texture(heh, uv + vec2( d1,  0.0) * inv_source_size);\n"
"	vec4 c5 = texture(heh, uv + vec2(-d1,  0.0) * inv_source_size);\n"
"	vec4 c6 = texture(heh, uv + vec2( 0.0,  d1) * inv_source_size);\n"
"	vec4 c7 = texture(heh, uv + vec2( 0.0, -d1) * inv_source_size);\n"
"	vec3 average = (ToDisplay(c0.rgb) + ToDisplay(c1.rgb) + ToDisplay(c2.rgb) + ToDisplay(c3.rgb)) * w0 +\n"
"		(ToDisplay(c4.rgb) + ToDisplay(c5.rgb) + ToDisplay(c6.rgb) + ToDisplay(c7.rgb)) * w1;\n"
"	float alpha = (c0.a + c1.a + c2.a + c3.a) * w0 + (c4.a + c5.a + c6.a + c7.a) * w1;\n"
"	return vec4(FromDisplay(average), clamp(alpha, 0.0, 1.0));\n"
"}\n"
"\n"
"void main()\n"
"{\n"
"	ivec2 dst = ivec2(gl_FragCoord.xy) - dest_origin;\n"
"	color = WronskiFilter(dst);\n"
"}\n"
"";

const char* bloomThresholdFragmentSrc =
"#version 330 core\n"
"\n"
"in vec2 outuv;\n"
"out vec4 color;\n"
"\n"
"uniform sampler2D heh;\n"
"uniform sampler2D depth_source;\n"
"uniform float gamma;\n"
"uniform float bloom_threshold;\n"
"uniform int use_depth_mask;\n"
"\n"
"vec3 ToDisplay(vec3 sourcecolor)\n"
"{\n"
"	return pow(max(sourcecolor, vec3(0.0)), vec3(gamma));\n"
"}\n"
"\n"
"vec3 FromDisplay(vec3 sourcecolor)\n"
"{\n"
"	return pow(max(sourcecolor, vec3(0.0)), vec3(1.0 / max(gamma, 0.0001)));\n"
"}\n"
"\n"
"vec3 SampleBloom(vec2 uv, vec2 step, float x, float y)\n"
"{\n"
"	vec2 sampleuv = uv + step * vec2(x, y);\n"
"	if (use_depth_mask != 0 && texture(depth_source, sampleuv).r >= 0.9999)\n"
"		return vec3(0.0);\n"
"	vec3 displaycolor = ToDisplay(texture(heh, sampleuv).rgb);\n"
"	float brightness = max(max(displaycolor.r, displaycolor.g), displaycolor.b);\n"
"	float threshold_range = max(1.0 - bloom_threshold, 0.0001);\n"
"	float amount = clamp((brightness - bloom_threshold) / threshold_range, 0.0, 1.0);\n"
"	return displaycolor * amount;\n"
"}\n"
"\n"
"vec3 SmoothBloomDownsample(vec2 baseuv, vec2 step)\n"
"{\n"
"	vec3 h0 = SampleBloom(baseuv, step, -1.0,  1.0);\n"
"	vec3 h1 = SampleBloom(baseuv, step,  1.0,  1.0);\n"
"	vec3 h2 = SampleBloom(baseuv, step, -1.0, -1.0);\n"
"	vec3 h3 = SampleBloom(baseuv, step,  1.0, -1.0);\n"
"	vec3 l0 = SampleBloom(baseuv, step, -2.0,  2.0);\n"
"	vec3 l1 = SampleBloom(baseuv, step,  0.0,  2.0);\n"
"	vec3 l2 = SampleBloom(baseuv, step,  2.0,  2.0);\n"
"	vec3 l3 = SampleBloom(baseuv, step, -2.0,  0.0);\n"
"	vec3 l4 = SampleBloom(baseuv, step,  0.0,  0.0);\n"
"	vec3 l5 = SampleBloom(baseuv, step,  2.0,  0.0);\n"
"	vec3 l6 = SampleBloom(baseuv, step, -2.0, -2.0);\n"
"	vec3 l7 = SampleBloom(baseuv, step,  0.0, -2.0);\n"
"	vec3 l8 = SampleBloom(baseuv, step,  2.0, -2.0);\n"
"	return (h0 + h1 + h2 + h3) * 0.125 + (l0 + l1 + l2 + l3 + l4 + l5 + l6 + l7 + l8) * 0.0555555;\n"
"}\n"
"\n"
"void main()\n"
"{\n"
"	vec2 step = vec2(1.0) / vec2(textureSize(heh, 0));\n"
"	color = vec4(FromDisplay(SmoothBloomDownsample(outuv, step)), 1.0);\n"
"}\n"
"";

