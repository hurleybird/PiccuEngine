const char* gtaoAOFragmentSrc =
"#version 450 core\n"
"\n"
"in vec2 outuv;\n"
"layout(location = 0) out vec4 ao_out;\n"
"\n"
"uniform sampler2D depth_tex;\n"
"uniform sampler2D noise_tex;\n"
"\n"
"//Projection helpers: pack (2/m00, 2/m11, -1/m00, -1/m11) so that\n"
"//  view.xy = (uv * proj_info.xy + proj_info.zw) * depth;\n"
"uniform vec4 proj_info;\n"
"uniform vec2 near_far;       // (nearZ, farZ)\n"
"uniform float radius;        // world-space radius\n"
"uniform float radius_pixels; // pixel-space radius when depth == 1\n"
"uniform float max_radius_pixels;\n"
"uniform float neg_inv_radius2;\n"
"uniform float angle_bias;\n"
"uniform vec2 inv_screen_size;\n"
"uniform vec2 ao_inv_screen_size;\n"
"uniform vec2 screen_size;\n"
"uniform vec2 noise_origin;\n"
"uniform int directions;\n"
"uniform int steps;\n"
"const float PI = 3.14159265358979323846;\n"
"\n"
"float linearize_depth(float d)\n"
"{\n"
"    //OpenGL clip-space z is [-1,1] after divide; FBO depth is [0,1].\n"
"    float z_n = 2.0 * d - 1.0;\n"
"    float n = near_far.x;\n"
"    float f = near_far.y;\n"
"    return (2.0 * n * f) / (f + n - z_n * (f - n));\n"
"}\n"
"\n"
"vec3 fetch_view_pos(vec2 uv)\n"
"{\n"
"    //Clamp only the texture lookup. Keeping the original UV for projection\n"
"    //prevents offscreen samples from collapsing onto the screen edge.\n"
"    vec2 depth_uv = clamp(uv, vec2(0.0), vec2(1.0) - ao_inv_screen_size * 0.5);\n"
"    float d = linearize_depth(texture(depth_tex, depth_uv).r);\n"
"    vec3 p;\n"
"    p.xy = (uv * proj_info.xy + proj_info.zw) * d;\n"
"    p.z = d;\n"
"    return p;\n"
"}\n"
"\n"
"vec3 min_diff(vec3 P, vec3 Pr, vec3 Pl)\n"
"{\n"
"    vec3 V1 = Pr - P;\n"
"    vec3 V2 = P - Pl;\n"
"    return (dot(V1, V1) < dot(V2, V2)) ? V1 : V2;\n"
"}\n"
"\n"
"vec3 reconstruct_normal(vec2 uv, vec3 P)\n"
"{\n"
"    vec3 Pr = fetch_view_pos(uv + vec2(ao_inv_screen_size.x, 0.0));\n"
"    vec3 Pl = fetch_view_pos(uv + vec2(-ao_inv_screen_size.x, 0.0));\n"
"    vec3 Pt = fetch_view_pos(uv + vec2(0.0, ao_inv_screen_size.y));\n"
"    vec3 Pb = fetch_view_pos(uv + vec2(0.0, -ao_inv_screen_size.y));\n"
"    //Cross(dx, dy) in our LH (x right, y up, z = +depth into scene) gives\n"
"    //+z = into the scene. We want a vector facing the camera, so negate.\n"
"    return -normalize(cross(min_diff(P, Pr, Pl), min_diff(P, Pt, Pb)));\n"
"}\n"
"\n"
"float falloff(float dist_sq)\n"
"{\n"
"    return dist_sq * neg_inv_radius2 + 1.0;\n"
"}\n"
"\n"
"float fetch_ao_weight(vec2 uv)\n"
"{\n"
"    vec2 depth_uv = clamp(uv, vec2(0.0), vec2(1.0) - ao_inv_screen_size * 0.5);\n"
"    return clamp(texture(depth_tex, depth_uv).g, 0.0, 1.0);\n"
"}\n"
"\n"
"float compute_gtao_horizon(vec3 P, vec3 N, vec2 dir, float step_pixels, float jitter, float side)\n"
"{\n"
"    float ray_pixels = jitter * step_pixels + 1.0;\n"
"    float horizon = 0.0;\n"
"    float average = 0.0;\n"
"    for (int s = 0; s < steps; ++s)\n"
"    {\n"
"        vec2 sample_uv = (round(ray_pixels * dir * side) * ao_inv_screen_size) + outuv;\n"
"        vec3 S = fetch_view_pos(sample_uv);\n"
"        float sample_weight = fetch_ao_weight(sample_uv);\n"
"        vec3 V = S - P;\n"
"        float VdotV = dot(V, V);\n"
"        float NdotV = dot(N, V) * inversesqrt(max(VdotV, 1e-6));\n"
"        float sample_horizon = max(NdotV - angle_bias, 0.0) * clamp(falloff(VdotV), 0.0, 1.0);\n"
"        sample_horizon *= sample_weight;\n"
"        horizon = max(horizon, sample_horizon);\n"
"        average += sample_horizon;\n"
"        ray_pixels += step_pixels;\n"
"    }\n"
"    average /= max(float(steps), 1.0);\n"
"    return mix(average, horizon, 0.75);\n"
"}\n"
"\n"
"vec2 fetch_noise(vec2 screen_pos)\n"
"{\n"
"    return texture(noise_tex, screen_pos / 4.0).rg;\n"
"}\n"
"\n"
"void main()\n"
"{\n"
"    vec3 P = fetch_view_pos(outuv);\n"
"    //Output linearized depth as the .y channel for the bilateral blur weight.\n"
"    float depth_for_blur = clamp(P.z / max(near_far.y, 1e-4), 0.0, 1.0);\n"
"\n"
"    //Avoid running on far-plane sky pixels.\n"
"    if (P.z >= near_far.y * 0.999)\n"
"    {\n"
"        ao_out = vec4(1.0, depth_for_blur, 0.0, 1.0);\n"
"        return;\n"
"    }\n"
"\n"
"    vec3 N = reconstruct_normal(outuv, P);\n"
"\n"
"    //Step size in pixels. Sample radius shrinks with depth (perspective).\n"
"    float step_pixels = min(radius_pixels / max(P.z, 1e-3), max_radius_pixels);\n"
"    step_pixels = step_pixels / float(steps + 1);\n"
"\n"
"    //Per-pixel noise: cos/sin angle in .r, sample jitter in .g.\n"
"    vec2 screen_pos = outuv * screen_size - noise_origin;\n"
"    vec2 rand = fetch_noise(screen_pos);\n"
"\n"
"    float ao = 0.0;\n"
"    int gtao_directions = max((directions + 1) / 2, 1);\n"
"    float alpha = PI / float(gtao_directions);\n"
"    for (int d = 0; d < gtao_directions; ++d)\n"
"    {\n"
"        float angle = alpha * (float(d) + rand.x);\n"
"        vec2 dir = vec2(cos(angle), sin(angle));\n"
"        float jitter = fract(rand.y + float(d) * 0.61803398875);\n"
"        ao += compute_gtao_horizon(P, N, dir, step_pixels, jitter, 1.0);\n"
"        ao += compute_gtao_horizon(P, N, dir, step_pixels, jitter, -1.0);\n"
"    }\n"
"    ao = ao / max(float(gtao_directions) * 2.0, 1.0);\n"
"    ao = ao / max(1.0 - angle_bias, 0.01);\n"
"\n"
"    ao = clamp(1.0 - ao, 0.0, 1.0);\n"
"\n"
"    ao_out = vec4(ao, depth_for_blur, 0.0, 1.0);\n"
"}\n"
"";

