const char* pixelMotionBlurFragmentSrc =
"#version 450 core\n"
"\n"
"in vec2 outuv;\n"
"out vec4 color;\n"
"\n"
"uniform sampler2D color_source;\n"
"uniform sampler2D velocity_source;\n"
"uniform sampler2D protection_mask;\n"
"uniform int use_protection_mask;\n"
"uniform vec2 velocity_uv_origin;\n"
"uniform vec2 velocity_uv_scale;\n"
"uniform float strength;\n"
"\n"
"void main()\n"
"{\n"
"	vec4 base = texture(color_source, outuv);\n"
"	vec2 mask_uv = velocity_uv_origin + outuv * velocity_uv_scale;\n"
"	if (use_protection_mask != 0)\n"
"	{\n"
"		vec4 mask = texture(protection_mask, mask_uv);\n"
"		float protection = clamp(max(mask.r, mask.g), 0.0, 1.0);\n"
"		if (protection >= 0.999)\n"
"		{\n"
"			color = base;\n"
"			return;\n"
"		}\n"
"	}\n"
"	vec2 velocity = texture(velocity_source, mask_uv).xy;\n"
"	vec2 blur = clamp(velocity * strength, vec2(-0.08), vec2(0.08));\n"
"	if (strength <= 0.0 || dot(blur, blur) < 0.0000000001)\n"
"	{\n"
"		color = base;\n"
"		return;\n"
"	}\n"
"	const int samples = 9;\n"
"	vec4 accum = vec4(0.0);\n"
"	for (int i = 0; i < samples; ++i)\n"
"	{\n"
"		float t = float(i) / float(samples - 1) - 0.5;\n"
"		vec2 sample_uv = clamp(outuv - blur * t, vec2(0.0), vec2(1.0));\n"
"		vec4 sample_color = texture(color_source, sample_uv);\n"
"		if (use_protection_mask != 0)\n"
"		{\n"
"			vec2 sample_mask_uv = velocity_uv_origin + sample_uv * velocity_uv_scale;\n"
"			vec4 sample_mask = texture(protection_mask, sample_mask_uv);\n"
"			float sample_protection = clamp(max(sample_mask.r, sample_mask.g), 0.0, 1.0);\n"
"			sample_color = mix(sample_color, base, sample_protection);\n"
"		}\n"
"		accum += sample_color;\n"
"	}\n"
"	vec4 blurred = accum / float(samples);\n"
"	if (use_protection_mask != 0)\n"
"	{\n"
"		vec4 mask = texture(protection_mask, mask_uv);\n"
"		float protection = clamp(max(mask.r, mask.g), 0.0, 1.0);\n"
"		color = mix(blurred, base, protection);\n"
"	}\n"
"	else\n"
"	{\n"
"		color = blurred;\n"
"	}\n"
"}\n"
"";

