const char* downsampleFragmentSrc =
"#version 450 core\n"
"\n"
"out vec4 color;\n"
"\n"
"uniform sampler2D heh;\n"
"uniform float gamma;\n"
"uniform ivec2 dest_origin;\n"
"uniform ivec2 source_visible_origin;\n"
"uniform ivec2 source_visible_size;\n"
"\n"
"vec3 ToDisplay(vec3 sourcecolor)\n"
"{\n"
"	return pow(max(sourcecolor, vec3(0.0)), vec3(gamma));\n"
"}\n"
"\n"
"vec3 FromDisplay(vec3 sourcecolor)\n"
"{\n"
"	return pow(max(sourcecolor, vec3(0.0)), vec3(1.0 / max(gamma, 0.0001)));\n"
"}\n"
"\n"
"vec4 WronskiFilter(ivec2 dst)\n"
"{\n"
"	const float d0 = 0.75777;\n"
"	const float d1 = 2.907;\n"
"	const float w0 = 0.37487566;\n"
"	const float w1 = -0.12487566;\n"
"	vec2 inv_source_size = 1.0 / vec2(textureSize(heh, 0));\n"
"	vec2 uv = (vec2(dst) * 2.0 + vec2(1.0)) * inv_source_size;\n"
"	vec2 uv_min = vec2(0.0);\n"
"	vec2 uv_max = vec2(1.0);\n"
"	if (source_visible_size.x > 0 && source_visible_size.y > 0)\n"
"	{\n"
"		uv_min = (vec2(source_visible_origin) + vec2(0.5)) * inv_source_size;\n"
"		uv_max = (vec2(source_visible_origin + source_visible_size) - vec2(0.5)) * inv_source_size;\n"
"	}\n"
"	vec4 c0 = texture(heh, clamp(uv + vec2( d0,  d0) * inv_source_size, uv_min, uv_max));\n"
"	vec4 c1 = texture(heh, clamp(uv + vec2(-d0,  d0) * inv_source_size, uv_min, uv_max));\n"
"	vec4 c2 = texture(heh, clamp(uv + vec2( d0, -d0) * inv_source_size, uv_min, uv_max));\n"
"	vec4 c3 = texture(heh, clamp(uv + vec2(-d0, -d0) * inv_source_size, uv_min, uv_max));\n"
"	vec4 c4 = texture(heh, clamp(uv + vec2( d1,  0.0) * inv_source_size, uv_min, uv_max));\n"
"	vec4 c5 = texture(heh, clamp(uv + vec2(-d1,  0.0) * inv_source_size, uv_min, uv_max));\n"
"	vec4 c6 = texture(heh, clamp(uv + vec2( 0.0,  d1) * inv_source_size, uv_min, uv_max));\n"
"	vec4 c7 = texture(heh, clamp(uv + vec2( 0.0, -d1) * inv_source_size, uv_min, uv_max));\n"
"	vec3 average = (ToDisplay(c0.rgb) + ToDisplay(c1.rgb) + ToDisplay(c2.rgb) + ToDisplay(c3.rgb)) * w0 +\n"
"		(ToDisplay(c4.rgb) + ToDisplay(c5.rgb) + ToDisplay(c6.rgb) + ToDisplay(c7.rgb)) * w1;\n"
"	float alpha = (c0.a + c1.a + c2.a + c3.a) * w0 + (c4.a + c5.a + c6.a + c7.a) * w1;\n"
"	return vec4(FromDisplay(average), clamp(alpha, 0.0, 1.0));\n"
"}\n"
"\n"
"void main()\n"
"{\n"
"	ivec2 dst = ivec2(gl_FragCoord.xy) - dest_origin;\n"
"	color = WronskiFilter(dst);\n"
"}\n"
"";

